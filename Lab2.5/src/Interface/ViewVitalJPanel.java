    private void viewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsActionPerformed
        // TODO add your handling code here:
        
        int selectedrow = tblVitalSign.getSelectedRow();
        
        if(selectedrow >=0)
        {
            VitalSigns vs= (VitalSigns)tblVitalSign.getValueAt(selectedrow, 0);
            bp.setText(String.valueOf(vs.getBloodPressure()));
            temp.setText(String.valueOf(vs.getTemperature()));
            pulsetxt.setText(String.valueOf(vs.getPulse()));
            datetxt.setText(vs.getDate());
        }
        else
            JOptionPane.showMessageDialog(null, "Please select a row");
    }//GEN-LAST:event_viewDetailsActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
         int selectedrow = tblVitalSign.getSelectedRow();
        
        if(selectedrow >=0)
        {
         VitalSigns vs= (VitalSigns)tblVitalSign.getValueAt(selectedrow, 0);
         vitalsignhistory.deleteVitals(vs);
         JOptionPane.showMessageDialog(null, "Vital sign has been deleted");
         populateTable();
        }
        else
           JOptionPane.showMessageDialog(null, "Please select a row");  
    }//GEN-LAST:event_deleteActionPerformed


