    public void populateTable()//display all records in the arraylist
    {
    DefaultTableModel  dtm = (DefaultTableModel)tblVitalSign.getModel();
    dtm.setRowCount(0);
    
    for(VitalSigns vs : vitalsignhistory.getVitalSignHistory())
    {
        Object row[]= new Object[2];
        row[0]=vs;
        row[1]=vs.getBloodPressure();
        dtm.addRow(row);
    }
    
    }

